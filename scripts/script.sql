CREATE DATABASE video_store; 
USE video_store;
DROP TABLE IF EXISTS `Movies`;
DROP TABLE IF EXISTS `RentRegisters`;
DROP TABLE IF EXISTS `Users`;
CREATE TABLE IF NOT EXISTS `RentRegisters` (`id` CHAR(36) BINARY NOT NULL , `idMovie` VARCHAR(255) NOT NULL, `action` VARCHAR(255) NOT NULL, `idUser` VARCHAR(255) NOT NULL, `quantity` INTEGER NOT NULL, `value` FLOAT NOT NULL, `endDate` VARCHAR(255) NOT NULL, `initDate` VARCHAR(255) NOT NULL, `createdAt` DATETIME NOT NULL, `updatedAt` DATETIME NOT NULL, PRIMARY KEY (`id`)) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS `Movies` (`id` CHAR(36) BINARY NOT NULL , `title` VARCHAR(255) NOT NULL, `director` VARCHAR(255) NOT NULL, `quantity` INTEGER NOT NULL, `createdAt` DATETIME NOT NULL, `updatedAt` DATETIME NOT NULL, PRIMARY KEY (`id`)) ENGINE=InnoDB;
CREATE TABLE IF NOT EXISTS `Users` (`id` CHAR(36) BINARY NOT NULL , `username` VARCHAR(255) NOT NULL, `email` VARCHAR(255) NOT NULL, `password` VARCHAR(255) NOT NULL, `createdAt` DATETIME NOT NULL, `updatedAt` DATETIME NOT NULL, PRIMARY KEY (`id`)) ENGINE=InnoDB;
